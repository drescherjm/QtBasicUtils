#include "qbuWidgetsPCH.h"

#include "qbuWidgets/qbuComboBoxInterface.h"
#include "qbuWidgets/qbuComboBox.h"
#include <QIcon>

/////////////////////////////////////////////////////////////////////////////////////////

qbuComboBoxInterface::qbuComboBoxInterface(QObject *parent /*= 0*/) : QObject(parent),
	m_bInitilaized(false)
{

}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuComboBoxInterface::name() const
{
	return "qbu::qbuComboBox";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuComboBoxInterface::group() const
{
	return "qbuWidgets";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuComboBoxInterface::toolTip() const
{
	return "Nothing to see here!";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuComboBoxInterface::whatsThis() const
{
	return tr("A ComboBox that has properties to simplify the data transfer between the form and a database.");
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuComboBoxInterface::includeFile() const
{
	return "qbuWidgets/qbuComboBox.h";
}

/////////////////////////////////////////////////////////////////////////////////////////

QIcon qbuComboBoxInterface::icon() const
{
	return QIcon();
}

/////////////////////////////////////////////////////////////////////////////////////////

bool qbuComboBoxInterface::isContainer() const
{
	return false;
}

/////////////////////////////////////////////////////////////////////////////////////////

QWidget * qbuComboBoxInterface::createWidget(QWidget *parent)
{
	return new qbu::qbuComboBox(parent);
}

/////////////////////////////////////////////////////////////////////////////////////////

bool qbuComboBoxInterface::isInitialized() const
{
	return m_bInitilaized;
}

/////////////////////////////////////////////////////////////////////////////////////////

void qbuComboBoxInterface::initialize(QDesignerFormEditorInterface *core)
{
	if (!m_bInitilaized) {
		m_bInitilaized = true;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuComboBoxInterface::domXml() const
{
	return
		"<ui language=\"c++\">\n"
		"<widget class = \"qbu::qbuComboBox\">\n"
		"<property name = \"text\" >\n"
		"<string>qbuComboBox</string>\n"
		"</property>\n"
		"</widget>\n"
		"</ui>\n";
}

