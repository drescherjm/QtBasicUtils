#include "qbuWidgetsPCH.h"

#include "qbuWidgets/qbuDateTimeEditorInterface.h"
#include "qbuWidgets/qbuDateTimeEditor.h"
#include <QIcon>

/////////////////////////////////////////////////////////////////////////////////////////

qbuDateTimeEditorInterface::qbuDateTimeEditorInterface(QObject *parent /*= 0*/) : QObject(parent),
	m_bInitilaized(false)
{

}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuDateTimeEditorInterface::name() const
{
	return "qbu::qbuDateTimeEditor";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuDateTimeEditorInterface::group() const
{
	return "qbuWidgets";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuDateTimeEditorInterface::toolTip() const
{
	return "Nothing to see here!";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuDateTimeEditorInterface::whatsThis() const
{
	return tr("A DateTimeEditor that has properties to simplify the data transfer between the form and a database.");
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuDateTimeEditorInterface::includeFile() const
{
	return "qbuWidgets/qbuDateTimeEditor.h";
}

/////////////////////////////////////////////////////////////////////////////////////////

QIcon qbuDateTimeEditorInterface::icon() const
{
	return QIcon();
}

/////////////////////////////////////////////////////////////////////////////////////////

bool qbuDateTimeEditorInterface::isContainer() const
{
	return false;
}

/////////////////////////////////////////////////////////////////////////////////////////

QWidget * qbuDateTimeEditorInterface::createWidget(QWidget *parent)
{
	return new qbu::qbuDateTimeEditor(parent);
}

/////////////////////////////////////////////////////////////////////////////////////////

bool qbuDateTimeEditorInterface::isInitialized() const
{
	return m_bInitilaized;
}

/////////////////////////////////////////////////////////////////////////////////////////

void qbuDateTimeEditorInterface::initialize(QDesignerFormEditorInterface *core)
{
	if (!m_bInitilaized) {
		m_bInitilaized = true;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuDateTimeEditorInterface::domXml() const
{
	return
		"<ui language=\"c++\">\n"
		"<widget class = \"qbu::qbuDateTimeEditor\">\n"
		"<property name = \"text\" >\n"
		"<string>qbuDateTimeEditor</string>\n"
		"</property>\n"
		"</widget>\n"
		"</ui>\n";
}

