#include "qbuWidgetsPCH.h"

#include "qbuWidgets/qbuTextEditInterface.h"
#include "qbuWidgets/qbuTextEdit.h"
#include <QIcon>

/////////////////////////////////////////////////////////////////////////////////////////

qbuTextEditInterface::qbuTextEditInterface(QObject *parent /*= 0*/) : QObject(parent),
	m_bInitilaized(false)
{

}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuTextEditInterface::name() const
{
	return "qbu::qbuTextEdit";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuTextEditInterface::group() const
{
	return "qbuWidgets";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuTextEditInterface::toolTip() const
{
	return "Nothing to see here!";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuTextEditInterface::whatsThis() const
{
	return tr("A TextEdit that has properties to simplify the data transfer between the form and a database.");
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuTextEditInterface::includeFile() const
{
	return "qbuWidgets/qbuTextEdit.h";
}

/////////////////////////////////////////////////////////////////////////////////////////

QIcon qbuTextEditInterface::icon() const
{
	return QIcon();
}

/////////////////////////////////////////////////////////////////////////////////////////

bool qbuTextEditInterface::isContainer() const
{
	return false;
}

/////////////////////////////////////////////////////////////////////////////////////////

QWidget * qbuTextEditInterface::createWidget(QWidget *parent)
{
	return new qbu::qbuTextEdit(parent);
}

/////////////////////////////////////////////////////////////////////////////////////////

bool qbuTextEditInterface::isInitialized() const
{
	return m_bInitilaized;
}

/////////////////////////////////////////////////////////////////////////////////////////

void qbuTextEditInterface::initialize(QDesignerFormEditorInterface *core)
{
	if (!m_bInitilaized) {
		m_bInitilaized = true;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuTextEditInterface::domXml() const
{
	return
		"<ui language=\"c++\">\n"
		"<widget class = \"qbu::qbuTextEdit\">\n"
		"<property name = \"text\" >\n"
		"<string>qbuTextEdit</string>\n"
		"</property>\n"
		"</widget>\n"
		"</ui>\n";
}

