QString logFileName(QString strFile,int nLine )
{
	//strFile.remove("..\\");
	strFile.remove(QRegExp("\\.\\.[/\\\\]")); // Removes ..\ or ../
	return QString("%1:%2").arg(strFile).arg(nLine);
}

