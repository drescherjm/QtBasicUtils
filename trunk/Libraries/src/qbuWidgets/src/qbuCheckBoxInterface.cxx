#include "qbuWidgetsPCH.h"

#include "qbuWidgets/qbuCheckBoxInterface.h"
#include "qbuWidgets/qbuCheckBox.h"
#include <QIcon>

/////////////////////////////////////////////////////////////////////////////////////////

qbuCheckBoxInterface::qbuCheckBoxInterface(QObject *parent /*= 0*/) : QObject(parent),
	m_bInitilaized(false)
{

}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuCheckBoxInterface::name() const
{
	return "qbu::qbuCheckBox";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuCheckBoxInterface::group() const
{
	return "qbuWidgets";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuCheckBoxInterface::toolTip() const
{
	return "Nothing to see here!";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuCheckBoxInterface::whatsThis() const
{
	return tr("A CheckBox that has properties to simplify the data transfer between the form and a database.");
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuCheckBoxInterface::includeFile() const
{
	return "qbuWidgets/qbuCheckBox.h";
}

/////////////////////////////////////////////////////////////////////////////////////////

QIcon qbuCheckBoxInterface::icon() const
{
	return QIcon();
}

/////////////////////////////////////////////////////////////////////////////////////////

bool qbuCheckBoxInterface::isContainer() const
{
	return false;
}

/////////////////////////////////////////////////////////////////////////////////////////

QWidget * qbuCheckBoxInterface::createWidget(QWidget *parent)
{
	return new qbu::qbuCheckBox(parent);
}

/////////////////////////////////////////////////////////////////////////////////////////

bool qbuCheckBoxInterface::isInitialized() const
{
	return m_bInitilaized;
}

/////////////////////////////////////////////////////////////////////////////////////////

void qbuCheckBoxInterface::initialize(QDesignerFormEditorInterface *core)
{
	if (!m_bInitilaized) {
		m_bInitilaized = true;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuCheckBoxInterface::domXml() const
{
	return
		"<ui language=\"c++\">\n"
		"<widget class = \"qbu::qbuCheckBox\">\n"
		"<property name = \"text\" >\n"
		"<string>qbuCheckBox</string>\n"
		"</property>\n"
		"</widget>\n"
		"</ui>\n";
}

