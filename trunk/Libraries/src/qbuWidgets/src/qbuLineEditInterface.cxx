#include "qbuWidgetsPCH.h"

#include "qbuWidgets/qbuLineEditInterface.h"
#include "qbuWidgets/qbuLineEdit.h"
#include <QIcon>

/////////////////////////////////////////////////////////////////////////////////////////

qbuLineEditInterface::qbuLineEditInterface(QObject *parent /*= 0*/) : QObject(parent),
	m_bInitilaized(false)
{

}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuLineEditInterface::name() const
{
	return "qbu::qbuLineEdit";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuLineEditInterface::group() const
{
	return "qbuWidgets";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuLineEditInterface::toolTip() const
{
	return "Nothing to see here!";
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuLineEditInterface::whatsThis() const
{
	return tr("A LineEdit that has properties to simplify the data transfer between the form and a database.");
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuLineEditInterface::includeFile() const
{
	return "qbuWidgets/qbuLineEdit.h";
}

/////////////////////////////////////////////////////////////////////////////////////////

QIcon qbuLineEditInterface::icon() const
{
	return QIcon();
}

/////////////////////////////////////////////////////////////////////////////////////////

bool qbuLineEditInterface::isContainer() const
{
	return false;
}

/////////////////////////////////////////////////////////////////////////////////////////

QWidget * qbuLineEditInterface::createWidget(QWidget *parent)
{
	return new qbu::qbuLineEdit(parent);
}

/////////////////////////////////////////////////////////////////////////////////////////

bool qbuLineEditInterface::isInitialized() const
{
	return m_bInitilaized;
}

/////////////////////////////////////////////////////////////////////////////////////////

void qbuLineEditInterface::initialize(QDesignerFormEditorInterface *core)
{
	if (!m_bInitilaized) {
		m_bInitilaized = true;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////

QString qbuLineEditInterface::domXml() const
{
	return
		"<ui language=\"c++\">\n"
		"<widget class = \"qbu::qbuLineEdit\">\n"
		"<property name = \"text\" >\n"
		"<string>qbuLineEdit</string>\n"
		"</property>\n"
		"</widget>\n"
		"</ui>\n";
}

