#include "QCmdLineFileList.h"

namespace QTUTILS {

/////////////////////////////////////////////////////////////////////////////////////////

	int QCmdLineFileList::size() const
	{
		return m_strListFiles.size();
	}

/////////////////////////////////////////////////////////////////////////////////////////'

	void QCmdLineFileList::push_back(const QString & str)
	{
		m_strListFiles.push_back(str);
	}

/////////////////////////////////////////////////////////////////////////////////////////'

	void QCmdLineFileList::clear()
	{
		m_strListFiles.clear();
	}

/////////////////////////////////////////////////////////////////////////////////////////




}; // namespace QTUTILS